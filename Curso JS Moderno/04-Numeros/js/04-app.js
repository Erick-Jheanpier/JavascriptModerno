//EL OBJETO mATH TE TRAE FORMULAS MATEMATICAS
let respuesta;

//valor pi
respuesta=Math.PI;
console.log(respuesta)

//redondear
respuesta=Math.round(3.5)
console.log(respuesta)

//raiz cuadrada
respuesta=Math.sqrt(84);
console.log(respuesta)

//valor absoluto
respuesta=Math.abs(-500)
console.log(respuesta)

//potencia
respuesta=Math.pow(8,3)
console.log(respuesta)

//minimo
respuesta=Math.min(1,2,3)
console.log(respuesta)

//max
respuesta=Math.max(1,2,3)
console.log(respuesta)

//aleatorio
respuesta=Math.random();
console.log(respuesta)


//aleatorio dentro de un rango
respuesta=Math.random()*100
console.log(respuesta)

